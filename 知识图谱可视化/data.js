    function stripPrefix(value) {
        return value.replace(/^\d+_/, "");
    }

    function parseTree(rawTree) {
        const nodes = [];
        const stack = [];
        const nodeById = new Map();
        const childBuckets = new Map();
        const lines = rawTree
            .split(/\r?\n/)
            .map((line) => line.replace(/\r/g, ""))
            .filter((line) => line.trim().length > 0);

        lines.forEach((line) => {
			const normalizedLine = line.replace(/ {4}/g, "\t").replace(/ {2}/g, "\t");
            const match = normalizedLine.match(/^(\t*)- (.+)$/);

            if (!match) {
                return;
            }

            const level = match[1].length;
            const rawName = match[2].trim();
            const codeMatch = rawName.match(/^(\d+)_/);
            const code = codeMatch ? codeMatch[1] : rawName;
            const parent = level === 0 ? null : stack[level - 1];
            const stageId = level === 0 ? code : stack[0].code;
            const id = parent ? `${parent.id}-${code}` : code;

            const node = {
                id,
                code,
                rawName,
                label: stripPrefix(rawName),
                level,
                parentId: parent ? parent.id : null,
                stageId,
                stage: Number(stageId),
                childCount: 0
            };

            stack[level] = node;
            stack.length = level + 1;
            nodes.push(node);
            nodeById.set(id, node);
            childBuckets.set(id, []);
        });

        nodes.forEach((node) => {
            if (!node.parentId) {
                return;
            }

            childBuckets.get(node.parentId).push(node.id);
            nodeById.get(node.parentId).childCount += 1;
        });

        nodes.forEach((node) => {
            const lineage = [];
            let current = node;

            while (current) {
                lineage.unshift(current.rawName);
                current = current.parentId ? nodeById.get(current.parentId) : null;
            }

            node.fullPath = lineage.join(" / ");
            node.breadcrumb = lineage.map(stripPrefix).join(" / ");
            node.children = childBuckets.get(node.id);
        });

        return { nodes, nodeById };
    }

